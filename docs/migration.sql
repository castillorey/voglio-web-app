    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    UNIQUE(voglio_id, user_id)
);

ALTER TABLE public.voglio_taken OWNER TO postgres;

ALTER TABLE public.voglio_taken ENABLE ROW LEVEL SECURITY;

