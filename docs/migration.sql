-- 2. ROW LEVEL SECURITY

ALTER TABLE public.category ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.size ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.voglio ENABLE ROW LEVEL SECURITY;

-- Category policies
DROP POLICY IF EXISTS "Enable insert for authenticated users only" ON public.category;
CREATE POLICY "Enable insert for authenticated users only" ON public.category
    FOR INSERT TO authenticated WITH CHECK (true);

DROP POLICY IF EXISTS "Enable users to view their own data only" ON public.category;
CREATE POLICY "Categories are viewable by all authenticated users" ON public.category
    FOR SELECT TO authenticated USING (is_private = false OR auth.uid() = user_id);

DROP POLICY IF EXISTS "Enable delete for users based on user_id" ON public.category;
CREATE POLICY "Enable delete for users based on user_id" ON public.category
    FOR DELETE USING (auth.uid() = user_id);

DROP POLICY IF EXISTS "Enable update for users based on user_id" ON public.category;
CREATE POLICY "Enable update for users based on user_id" ON public.category
    FOR UPDATE USING (auth.uid() = user_id);

-- Voglio policies
DROP POLICY IF EXISTS "Enable delete for users based on user_id" ON public.voglio;
CREATE POLICY "Enable delete for users based on user_id" ON public.voglio
    FOR DELETE USING (auth.uid() = user_id);

DROP POLICY IF EXISTS "Enable insert for authenticated users only" ON public.voglio;
DROP POLICY IF EXISTS "Enable insert for users based on user_id" ON public.voglio;
CREATE POLICY "Enable insert for authenticated users only" ON public.voglio
    FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Enable insert for users based on user_id" ON public.voglio
    FOR INSERT WITH CHECK (auth.uid() = user_id);

DROP POLICY IF EXISTS "Enable users to view their own data only" ON public.voglio;
CREATE POLICY "Voglios are viewable by all authenticated users" ON public.voglio
    FOR SELECT TO authenticated USING (is_private = false OR auth.uid() = user_id);

DROP POLICY IF EXISTS "Policy with authenticated user only" ON public.voglio;
CREATE POLICY "Voglios can be updated by any authenticated user" ON public.voglio
    FOR UPDATE TO authenticated USING (true);

-- Size policies (read-only for all authenticated users)
CREATE POLICY "Enable read access for all users" ON public.size
    FOR SELECT USING (true);

-- Profiles RLS
